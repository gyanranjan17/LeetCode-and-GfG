class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        // Your code here
        if(n==1) return arr[0];
        int dp1=0,dp2=arr[0],curr=0;
        for(int i=1;i<n;i++){
            int p=arr[i];
            if(i>1)
            p+=dp1;
            int np=dp2;
            curr=max(p,np);
            dp1=dp2;
            dp2=curr;
        }
        return curr;
    }
};

